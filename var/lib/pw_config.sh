echo "PW_CONTAINER_SCRIPTS_DIR=$PW_CONTAINER_SCRIPTS_DIR" >> $PW_LIB_DIR"/pw.env"
echo "PW_CONTAINER_BACKUP_PROD_DIR=$PW_CONTAINER_BACKUP_PROD_DIR" >> $PW_LIB_DIR"/pw.env"
echo "PW_CONTAINER_PLUGINS_DIR=$PW_CONTAINER_PLUGINS_DIR" >> $PW_LIB_DIR"/pw.env"
echo "PW_CONTAINER_THEMES_DIR=$PW_CONTAINER_THEMES_DIR" >> $PW_LIB_DIR"/pw.env"
